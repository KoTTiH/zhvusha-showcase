class TestCanHandleDoesNotShadowSiblings:
    """SpecCommand owns ``/spec`` and ``/spec <subcommand>`` — must NOT match
    sibling triggers like ``/spec_run`` (Editor) or ``/spec_create`` (Architect).

    Otherwise dispatcher returns the SpecCommand help instead of routing the
    Telegram message to the right skill (regression observed 2026-04-26).
    """

    async def test_spec_run_returns_zero(self, tmp_path: Path) -> None:
        skill = _make_skill(tmp_path / "tasks")
        confidence = await skill.can_handle("/spec_run weather-skill", _ctx())
        assert confidence == 0.0

    async def test_spec_create_returns_zero(self, tmp_path: Path) -> None:
        skill = _make_skill(tmp_path / "tasks")
        confidence = await skill.can_handle("/spec_create new feature", _ctx())
        assert confidence == 0.0

    async def test_bare_spec_still_matches(self, tmp_path: Path) -> None:
        skill = _make_skill(tmp_path / "tasks")
        confidence = await skill.can_handle("/spec", _ctx())
        assert confidence >= 0.9

    async def test_spec_subcommands_still_match(self, tmp_path: Path) -> None:
        skill = _make_skill(tmp_path / "tasks")
        for cmd in (
            "/spec list",
            "/spec show foo",
            "/spec approve foo",
            "/spec reject foo bar",
        ):
            confidence = await skill.can_handle(cmd, _ctx())
            assert confidence >= 0.9, f"failed for {cmd!r}: got {confidence}"

    async def test_execute_unknown_subcommand_returns_help(
        self, tmp_path: Path
    ) -> None:
        """Defence-in-depth: even if can_handle let through, execute must
        not echo HELP for ``/spec_run`` (which would mask the routing bug)."""
        skill = _make_skill(tmp_path / "tasks")
        result = await skill.execute("/spec_run weather-skill", _ctx())
        # Either no response (let other skills take over) or a friendly
        # "unknown" message — but NOT the full SpecCommand help text.
        assert "/spec list" not in result.response or not result.success


