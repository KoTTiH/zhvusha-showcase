def _git_stdout(repo: Path, *args: str) -> str:
    import subprocess

    return subprocess.run(  # noqa: S603 — args literal
        ["git", *args],  # noqa: S607 — git on PATH
        cwd=repo,
        check=True,
        capture_output=True,
        text=True,
    ).stdout


class TestApproveAutoCommitsYaml:
    """Phase 19 — when ``repo_root`` is supplied, ``/spec approve`` must
    auto-commit the yaml mutation so the next ``/spec_run`` starts on a
    clean worktree.

    Pre-Phase-19, every approve produced a modified-but-uncommitted
    yaml; ``BranchManager._assert_clean_tree`` then refused
    ``/spec_run``, forcing operators to land the mutation manually.
    """

    async def test_approve_lands_yaml_commit(self, tmp_git_repo: Path) -> None:
        """After approve, the yaml mutation lives in HEAD, not in the
        worktree."""
        from src.skills.spec_command.skill import SpecCommandSkill

        tasks_dir = tmp_git_repo / "tasks"
        spec_path = _write_spec(tasks_dir, _spec_dict(slug="weather-skill"))
        # Land the initial spec under HEAD so we have a clean baseline.
        subprocess_run_git(tmp_git_repo, "add", "-A")
        subprocess_run_git(tmp_git_repo, "commit", "-m", "init spec")

        skill = SpecCommandSkill(
            tasks_dir=tasks_dir,
            admin_user_id=12345,
            intent_classifier=None,
            repo_root=tmp_git_repo,
        )
        result = await skill.execute("/spec approve weather-skill", _ctx())

        assert result.success
        # Working tree clean — mutation is in HEAD now.
        porcelain = _git_stdout(tmp_git_repo, "status", "--porcelain")
        assert porcelain == "", f"expected clean tree, got: {porcelain!r}"
        # Spec yaml status is approved on disk.
        reloaded = yaml.safe_load(spec_path.read_text())
        assert reloaded["status"] == "approved"
        # HEAD message references the slug.
        msg = _git_stdout(tmp_git_repo, "log", "-1", "--pretty=%B")
        assert "weather-skill" in msg

    async def test_approve_without_repo_root_leaves_worktree_dirty_default(
        self, tmp_path: Path
    ) -> None:
        """Backward compat: existing callers without ``repo_root=`` still
        get the pre-Phase-19 behaviour — yaml is mutated on disk but no
        git operation runs."""
        tasks_dir = tmp_path / "tasks"
        spec_path = _write_spec(tasks_dir, _spec_dict(slug="weather-skill"))

        skill = _make_skill(tasks_dir)  # no repo_root → opt-out
        result = await skill.execute("/spec approve weather-skill", _ctx())

        assert result.success
        reloaded = yaml.safe_load(spec_path.read_text())
        assert reloaded["status"] == "approved"
        # No assertion on git — there's no repo here.


