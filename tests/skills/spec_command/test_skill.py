"""Behaviour tests for SpecCommandSkill — list/show/approve/reject + intent.

Uses tmp_path-based tasks/ directory, dummy ``Bot`` mock, and an injected
intent-classifier callable so the skill can stay LLM-free in tests
(per AGENTS.md rule 0 — only Haiku in tests, and these contract-level
tests use no LLM at all).
"""

from __future__ import annotations

from datetime import datetime
