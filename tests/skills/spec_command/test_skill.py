async def test_spec_approve_bundle_records_scope_for_multiple_specs(
    tmp_git_repo: Path,
) -> None:
    from src.skills.spec_command.skill import SpecCommandSkill

    tasks_dir = tmp_git_repo / "tasks"
    alpha_path = _write_spec(
        tasks_dir,
        _spec_dict(
            slug="alpha-architecture",
            status="pending_approval",
            tier=3,
            title="Alpha architecture change",
        ),
    )
    bravo_spec = _spec_dict(
        slug="bravo-safety",
        status="pending_approval",
        tier=3,
        title="Bravo safety change",
    )
    bravo_spec["chat_context"] = ["Никита: это уже обсуждали как один пакет."]
    bravo_path = _write_spec(tasks_dir, bravo_spec)
    subprocess_run_git(tmp_git_repo, "add", "-A")
    subprocess_run_git(tmp_git_repo, "commit", "-m", "init pending specs")
    head_before = _git_stdout(tmp_git_repo, "rev-parse", "HEAD").strip()

    skill = SpecCommandSkill(
        tasks_dir=tasks_dir,
        admin_user_id=12345,
        intent_classifier=None,
        repo_root=tmp_git_repo,
    )

    result = await skill.execute(
        "/spec approve-bundle alpha-architecture bravo-safety "
        '--scope "Tier 3 self-coding approval packet" '
        '--reason "avoid micro-approval stalls while keeping destructive actions separate"',
        _ctx(),
    )

    assert result.success
    assert _git_stdout(tmp_git_repo, "rev-parse", "HEAD").strip() == head_before
    assert "approved" in result.response
    assert "spec_run" in result.response

    for path in (alpha_path, bravo_path):
        reloaded = yaml.safe_load(path.read_text())
