pytestmark = pytest.mark.contract


def _spec_dict(
    *,
    slug: str = "weather-skill",
    status: str = "pending_approval",
    tier: int = 1,
    title: str = "Add weather skill",
) -> dict[str, Any]:
    return {
        "slug": slug,
        "title": title,
        "created_at": "2026-04-26T12:00:00+00:00",
        "created_by": "zhvusha",
        "tier": tier,
        "goal": "Дать Жвуше команду /weather, возвращающую температуру по городу.",
        "failing_test": {
            "file": f"tests/skills/{slug}/test_contract.py",
            "name": "test_returns_temp",
            "spec": "Mock API → response contains '12.5'.",
        },
        "whitelist_paths": [
            f"src/skills/{slug}/__init__.py",
            f"src/skills/{slug}/skill.py",
        ],
        "blast_radius": ["new skill, no existing skill touched"],
        "rollback_path": ["git revert", f"rm -rf src/skills/{slug}/"],
        "preserve_behavior": [
            "Existing skills, fallbacks, tests and chat behaviour stay intact.",
        ],
        "allowed_simplifications": [],
        "rationale": "Spec is generated by Жвуша and needs archive evidence.",
        "source_provenance": [
            {
                "url": "local:test",
                "source_type": "local_repo",
                "trust_tier": "direct",
                "claim": "Test helper generated this spec.",
            }
        ],
        "status": status,
    }


def _write_spec(tasks_dir: Path, spec: dict[str, Any]) -> Path:
    tasks_dir.mkdir(parents=True, exist_ok=True)
    path = tasks_dir / f"2026-04-26-{spec['slug']}.yaml"
    path.write_text(yaml.safe_dump(spec, allow_unicode=True, sort_keys=False))
    return path


def _make_skill(tasks_dir: Path, *, admin_user_id: int = 12345) -> Any:
    from src.skills.spec_command.skill import SpecCommandSkill

    return SpecCommandSkill(
        tasks_dir=tasks_dir,
        admin_user_id=admin_user_id,
        intent_classifier=None,  # keyword-only fallback for these tests
    )


def _ctx(
    *,
    user_id: int = 12345,
    mode: str = "personal",
    bot: Any = None,
    metadata: dict[str, Any] | None = None,
) -> Any:
    from src.skills.base import AgentContext

    return AgentContext(
        user_id=user_id,
        chat_id=user_id,
        mode=mode,  # type: ignore[arg-type]
        bot=bot,
        metadata=metadata or {},
    )


