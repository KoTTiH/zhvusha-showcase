def _editor_writes_attempts(
    contents: list[str],
    *,
    prompts: list[str] | None = None,
    message_prefix: str = "attempt",
):  # type: ignore[no-untyped-def]
    """Build an SDK runner that writes one content value per invocation."""
    from src.skills.implement_spec.sdk_runner import EditorSdkResult

    async def runner(*args: Any, **kwargs: Any) -> Any:
        del args
        if prompts is not None:
            prompts.append(kwargs["user_prompt"])
            attempt = len(prompts)
        else:
            attempt = 1
        content = contents[min(attempt - 1, len(contents) - 1)]
        cwd = kwargs["cwd"]
        rel = kwargs["whitelist_paths"][0]
        full = cwd / rel
        full.parent.mkdir(parents=True, exist_ok=True)
        full.write_text(content, encoding="utf-8")
        return EditorSdkResult(text=f"{message_prefix} {attempt}")

    return runner


