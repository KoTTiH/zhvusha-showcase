class TestExecuteHappyPath:
    async def test_creates_branch_and_commits(self, tmp_git_repo: Path) -> None:
        tasks = tmp_git_repo / "tasks"
        spec_path = _write_spec(
            tasks,
            slug="weather-skill",
            status="approved",
            whitelist_paths=[
                "src/skills/weather_skill/skill.py",
                "tasks/2026-04-26-weather-skill.yaml",
            ],
        )
        # Commit the spec file as Никита so the SDK starts from a clean tree.
        _git(tmp_git_repo, "add", str(spec_path.relative_to(tmp_git_repo)))
        _git(tmp_git_repo, "commit", "-m", "spec: weather-skill")

        skill = _make_skill(
            repo_root=tmp_git_repo,
            sdk_runner=_editor_writes_files(),
        )
        result = await skill.execute("/spec_run weather-skill", _ctx())
        assert result.success, result.response

        # Self-coding must not leave the live repo checked out on a
        # long-lived ``zhvusha/<slug>`` branch.
        branch = _git(tmp_git_repo, "rev-parse", "--abbrev-ref", "HEAD").stdout.strip()
        assert branch == "main"
        self_branches = _git(
            tmp_git_repo, "branch", "--list", "zhvusha/weather-skill*"
        ).stdout
        assert self_branches.strip() == ""

        # Commit landed under zhvusha-coder author.
        author = _git(tmp_git_repo, "log", "-1", "--pretty=%ae").stdout.strip()
        assert author == "zhvusha@local"

        # Spec file updated to status=done.
        from src.skills.spec_command.parser import SpecModel, SpecStatus

        reread = SpecModel.model_validate(
            yaml.safe_load(spec_path.read_text(encoding="utf-8"))
        )
        assert reread.status == SpecStatus.DONE
        assert reread.branch == "main"
        assert reread.commit_sha
        _git(tmp_git_repo, "cat-file", "-e", f"{reread.commit_sha}^{{commit}}")
        assert set(result.metadata["changed_files"]) == {
            "src/skills/weather_skill/skill.py",
            "tasks/2026-04-26-weather-skill.yaml",
        }
        assert result.metadata["backend"] == "codex_cli"
        assert result.metadata["tests"] == [
            "commit gate: tests, style and types passed",
            "test_count_delta: +0",
        ]
        assert result.metadata["failed_repairs"] == []
        assert result.metadata["risk_review"]
        assert result.metadata["reasoning_summary"] == "done"

    async def test_working_tree_clean_after_cycle(self, tmp_git_repo: Path) -> None:
        """Bug regression: after a successful cycle the spec.yaml status
        update must land in a commit, not be left dirty in the worktree.

        Otherwise the next ``/spec_run`` trips ``BranchManager._assert_clean_tree``
        and the agent can't run two cycles in a row without a manual commit.
        """
        tasks = tmp_git_repo / "tasks"
        spec_path = _write_spec(
            tasks,
            slug="weather-skill",
            status="approved",
            whitelist_paths=[
                "src/skills/weather_skill/skill.py",
                "tasks/2026-04-26-weather-skill.yaml",
            ],
        )
        _git(tmp_git_repo, "add", str(spec_path.relative_to(tmp_git_repo)))
        _git(tmp_git_repo, "commit", "-m", "spec: weather-skill")

        skill = _make_skill(
            repo_root=tmp_git_repo,
            sdk_runner=_editor_writes_files(),
        )
        result = await skill.execute("/spec_run weather-skill", _ctx())
        assert result.success, result.response

        porcelain = _git(tmp_git_repo, "status", "--porcelain").stdout.strip()
        assert porcelain == "", (
            f"working tree dirty after cycle:\n{porcelain}\n"
            "Expected the spec.yaml status update to be committed."
        )

