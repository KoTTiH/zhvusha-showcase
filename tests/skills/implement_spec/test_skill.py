"""Contract + chain tests for ``ImplementSpecSkill`` (Phase 13).

The skill orchestrates the Editor cycle: validate the approved spec,
gate it through ``CapsEnforcer``, simulate when self-coding is off,
otherwise create the branch, invoke the Editor SDK adapter, commit
through ``CommitRunner``, and write the result back to the spec file.

Heavy dependencies (``BranchManager``/``CommitRunner``) work against the
``tmp_git_repo`` fixture from ``conftest.py``; the SDK runner is always
a mock so no external code-agent session is spawned.
"""

