def _write_spec(
    tasks_dir: Path,
    *,
    slug: str = "weather-skill",
    status: str = "approved",
    tier: int = 1,
    whitelist_paths: list[str] | None = None,
    extra: dict[str, Any] | None = None,
) -> Path:
    if whitelist_paths is None:
        whitelist_paths = [f"src/skills/{slug.replace('-', '_')}/skill.py"]
    payload: dict[str, Any] = {
        "slug": slug,
        "title": f"Add {slug}",
        "created_at": "2026-04-26T12:00:00+00:00",
        "created_by": "zhvusha",
        "tier": tier,
        "goal": (
            "Дать Жвуше команду /weather для admin_user_id. "
            "Проверка через failing test."
        ),
        "failing_test": {
            "file": f"tests/skills/{slug.replace('-', '_')}/test_contract.py",
            "name": "test_x",
            "spec": "Mock API call → assert response.",
        },
        "whitelist_paths": whitelist_paths,
        "blast_radius": ["new skill, no existing skill touched"],
        "rollback_path": ["git revert", f"rm -rf src/skills/{slug}/"],
        "preserve_behavior": [
            "Existing skills, fallbacks, tests and chat behaviour stay intact.",
        ],
        "allowed_simplifications": [],
        "rationale": "Spec is generated by Жвуша and needs archive evidence.",
        "source_provenance": [
            {
                "url": "local:test",
                "source_type": "local_repo",
                "trust_tier": "direct",
                "claim": "Test helper generated this spec.",
            }
        ],
        "status": status,
    }
    if extra:
        payload.update(extra)
    if status in {"approved", "in_progress", "done"}:
        payload["approved_at"] = "2026-04-26T12:30:00+00:00"
        payload["approved_by"] = "nikita"
    tasks_dir.mkdir(parents=True, exist_ok=True)
    path = tasks_dir / f"2026-04-26-{slug}.yaml"
    path.write_text(yaml.safe_dump(payload, sort_keys=False), encoding="utf-8")
    return path


def _make_skill(  # type: ignore[no-untyped-def]
    *,
    repo_root: Path,
    tasks_dir: Path | None = None,
    self_coding_enabled: bool = True,
    self_coding_max_tier: int = 2,
    caps_allowed: bool = True,
    sdk_runner: Any = None,
    block_publisher: Any = None,
    cycle_analyzer: Any = None,
    formal_gate: Any = None,
    reviewer: Any = None,
    adversarial_provider: Any = None,
    env_guard: Any = None,
):
    from src.skills.implement_spec.branch_manager import BranchManager
    from src.skills.implement_spec.caps_enforcer import (
        CapsCheckResult,
        CapsEnforcer,
    )
    from src.skills.implement_spec.commit_runner import CommitRunner
    from src.skills.implement_spec.sdk_runner import EditorSdkResult
    from src.skills.implement_spec.skill import ImplementSpecSkill

    caps = AsyncMock(spec=CapsEnforcer)
    caps.check = AsyncMock(
        return_value=CapsCheckResult(
            allowed=caps_allowed,
            reason=None if caps_allowed else "лимит исчерпан",
        )
    )
    caps.record = AsyncMock(return_value=None)

    runner = sdk_runner or AsyncMock(return_value=EditorSdkResult(text="ok"))

    return ImplementSpecSkill(
        tasks_dir=tasks_dir or (repo_root / "tasks"),
        project_root=repo_root,
        admin_user_id=12345,
        self_coding_enabled=self_coding_enabled,
        self_coding_max_tier=self_coding_max_tier,
        caps_enforcer=caps,
        branch_manager=BranchManager(repo_root=repo_root, base_branch="main"),
        commit_runner=CommitRunner(repo_root=repo_root),
        sdk_runner=runner,
        block_publisher=block_publisher,
        cycle_analyzer=cycle_analyzer,
        formal_gate=formal_gate,
        reviewer=reviewer,
        adversarial_provider=adversarial_provider,
        env_guard=env_guard,
    )


def _git(repo: Path, *args: str) -> subprocess.CompletedProcess[str]:
    return subprocess.run(  # noqa: S603 — args literal, repo is tmp_path
        ["git", *args],  # noqa: S607 — git on PATH
        cwd=repo,
        check=True,
        capture_output=True,
        text=True,
    )


