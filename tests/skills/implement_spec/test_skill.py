def _editor_writes_files(*, message: str = "done"):  # type: ignore[no-untyped-def]
    """Build an SDK runner that pretends the Editor wrote each whitelist file."""
    from src.skills.implement_spec.sdk_runner import EditorSdkResult

    async def runner(*args: Any, **kwargs: Any) -> Any:
        del args
        cwd = kwargs["cwd"]
        for rel in kwargs["whitelist_paths"]:
            full = cwd / rel
            full.parent.mkdir(parents=True, exist_ok=True)
            full.write_text("editor wrote this\n", encoding="utf-8")
        return EditorSdkResult(text=message)

    return runner


