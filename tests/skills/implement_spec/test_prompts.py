class TestSystemPromptExistingTestsToUpdate:
    """System prompt must teach the Editor that
    ``spec.existing_tests_to_update`` is the only legitimate channel for
    mutating an existing test, and even so only within the
    ``allowed_changes`` envelope.

    Phase 16 — Architect declares legitimate test mutations at spec
    time when the spec extends a finite collection behind a hidden
    contract test. Without this rule in the system prompt the Editor's
    blanket "existing tests are immutable" instruction collides with
    the spec's explicit list, producing either an unjustified refusal
    or a free-for-all rewrite. Both are wrong — the prompt must
    distinguish the two cases.

    The behaviour depends on whether the list is empty or non-empty,
    so ``_build_system_prompt`` must take it as an argument.
    """

    @pytest.mark.parametrize("kind_name", ["FEATURE", "FIX", "DOCS"])
    def test_empty_list_keeps_existing_tests_immutable(self, kind_name: str) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import SpecKind

        kind = SpecKind[kind_name]
        prompt = ImplementSpecSkill._build_system_prompt(
            kind=kind, existing_tests_to_update=[]
        ).lower()
        # Empty list ⇒ all existing tests immutable, same as pre-Phase-16.
        assert "existing test" in prompt
        assert any(
            phrase in prompt
            for phrase in ("never modify", "do not modify", "don't modify")
        )

    @pytest.mark.parametrize("kind_name", ["FEATURE", "FIX", "DOCS"])
    def test_nonempty_list_softens_to_only_listed_tests(self, kind_name: str) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import (
            ExistingTestUpdate,
            SpecKind,
        )

        kind = SpecKind[kind_name]
        prompt = ImplementSpecSkill._build_system_prompt(
            kind=kind,
            existing_tests_to_update=[
                ExistingTestUpdate(
                    path="tests/research/test_research_service.py",
                    test_name="TestResearchPresets.test_four_presets_defined",
                    reason="adding bug_investigation breaks fixed-set assertion",
                    allowed_changes="add 'bug_investigation' to the asserted set",
                )
            ],
        ).lower()
        # Must reference the channel by name.
        assert "existing_tests_to_update" in prompt
        # Must explicitly say outside-the-list is still forbidden — the
        # softening is *exception-only*, not a reset of the rule.
        assert any(
            phrase in prompt
            for phrase in (
                "outside this list",
                "not in this list",
                "outside the list",
                "any test not in",
                "tests not listed",
                "any other existing test",
            )
        )

    def test_nonempty_list_includes_each_path(self) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import (
            ExistingTestUpdate,
            SpecKind,
        )

        prompt = ImplementSpecSkill._build_system_prompt(
            kind=SpecKind.FEATURE,
            existing_tests_to_update=[
                ExistingTestUpdate(
                    path="tests/research/test_research_service.py",
                    test_name="TestResearchPresets.test_four_presets_defined",
                    reason="extends PRESETS",
                    allowed_changes="add the new entry",
                ),
                ExistingTestUpdate(
                    path="tests/skills/research/test_presets.py",
                    test_name="test_baseline_count",
                    reason="extends PRESETS",
                    allowed_changes="bump expected count by 1",
                ),
            ],
        )
        assert "tests/research/test_research_service.py" in prompt
        assert "tests/skills/research/test_presets.py" in prompt

    def test_nonempty_list_includes_each_test_name(self) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import (
            ExistingTestUpdate,
            SpecKind,
        )

