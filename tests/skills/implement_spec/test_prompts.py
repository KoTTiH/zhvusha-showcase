"""Editor prompt content tests (Phase 15 calibration).

The Editor's user prompt is the only context the SDK sees besides the
system prompt and the spec object. Two SpecModel fields were silently
dropped in Phase 13 — closing those holes here so the next live cycle
doesn't replay them.

Critical holes addressed:

* ``research_findings`` (KB #72 / #71) — Architect spends a budget on
  research before writing the spec. If those citations don't reach the
  Editor, it re-derives the same KB facts (or, worse, ignores them and
  reinvents the wheel).
* ``failed_attempts`` — populated by ``_mark_failed`` on every red
  cycle. On retry the Editor must see what already broke; otherwise it
  repeats the same path.

Tests reuse the ``_spec`` helper pattern from ``test_dry_run.py`` —
``SpecModel`` constructor, no fixture file required.
"""

from __future__ import annotations

from datetime import UTC, datetime
from typing import Any

import pytest

pytestmark = pytest.mark.contract


def _spec(**overrides: Any) -> Any:
    from src.skills.spec_command.parser import (
        FailingTest,
        SpecModel,
    )

    base: dict[str, Any] = {
        "slug": "weather-skill",
        "title": "Add weather skill",
        "created_at": datetime(2026, 4, 26, 12, 0, tzinfo=UTC),
        "created_by": "zhvusha",
        "tier": 1,
        "goal": (
            "Дать Жвуше команду /weather для admin_user_id. "
            "Проверка через failing test."
        ),
        "failing_test": FailingTest(
            file="tests/skills/weather/test_contract.py",
            name="test_returns_temp",
            spec="Mock API → response contains '12.5'.",
        ),
        "whitelist_paths": [
            "src/skills/weather/__init__.py",
            "src/skills/weather/skill.py",
            "tests/skills/weather/test_contract.py",
        ],
        "blast_radius": ["new skill, no existing skill touched"],
        "rollback_path": ["git revert", "rm -rf src/skills/weather/"],
        "preserve_behavior": [
            "Existing skills, fallbacks, tests and chat behaviour stay intact.",
        ],
        "allowed_simplifications": [],
        "rationale": "Spec is generated by Жвуша and needs archive evidence.",
        "source_provenance": [
            {
                "url": "local:test",
                "source_type": "local_repo",
                "trust_tier": "direct",
                "claim": "Test helper generated this spec.",
            }
        ],
    }
    base.update(overrides)
    return SpecModel(**base)


