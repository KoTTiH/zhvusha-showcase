class TestSystemPromptTddInvariants:
    """System prompt branches by ``spec.kind`` so the Editor knows
    whether existing tests can be touched.

    FEATURE / FIX / DOCS: existing tests are immutable; if the test
    can't go green by editing code in the whitelist, STOP.

    REFACTOR: existing tests may be updated *structurally* (renamed
    imports, renamed symbol references, updated call signatures) — but
    NEVER assertion logic / expected values.

    Tests live in ``whitelist_paths`` (Editor needs them in RED-phase to
    create new files), so neither the PreToolUse hook nor pre-commit
    catches a "fix" that flips an existing test's assertions to go
    green. The system prompt is the only soft barrier.
    """

    @pytest.mark.parametrize("kind_name", ["FEATURE", "FIX", "DOCS"])
    def test_test_immutable_kinds_forbid_modifying_existing_tests(
        self, kind_name: str
    ) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import SpecKind

        kind = SpecKind[kind_name]
        prompt = ImplementSpecSkill._build_system_prompt(kind=kind).lower()
        assert "existing test" in prompt or "existing tests" in prompt
        assert any(
            phrase in prompt
            for phrase in (
                "never modify",
                "do not modify",
                "don't modify",
                "never edit existing test",
            )
        )

    @pytest.mark.parametrize("kind_name", ["FEATURE", "FIX", "DOCS"])
    def test_test_immutable_kinds_direct_to_fix_code(self, kind_name: str) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import SpecKind

        kind = SpecKind[kind_name]
        prompt = ImplementSpecSkill._build_system_prompt(kind=kind).lower()
        assert "fix the code" in prompt or "fix code, not the test" in prompt

    def test_refactor_kind_allows_structural_test_edits(self) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import SpecKind

        prompt = ImplementSpecSkill._build_system_prompt(kind=SpecKind.REFACTOR).lower()
        # Allows structural updates to existing tests.
        assert any(
            term in prompt
            for term in ("renamed import", "renamed symbol", "structural")
        )

    def test_refactor_kind_still_forbids_assertion_edits(self) -> None:
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import SpecKind

        prompt = ImplementSpecSkill._build_system_prompt(kind=SpecKind.REFACTOR).lower()
        # Even in refactor mode, assertion logic is still off-limits.
        assert "assertion" in prompt
        assert any(
            phrase in prompt
            for phrase in ("never", "do not", "don't", "must not", "forbid")
        )

    def test_default_call_uses_feature_kind(self) -> None:
        """Backwards-compat: ``_build_system_prompt()`` without args
        defaults to the strictest kind (FEATURE)."""
        from src.skills.implement_spec.skill import ImplementSpecSkill
        from src.skills.spec_command.parser import SpecKind

        default = ImplementSpecSkill._build_system_prompt()
        feature = ImplementSpecSkill._build_system_prompt(kind=SpecKind.FEATURE)
        assert default == feature


