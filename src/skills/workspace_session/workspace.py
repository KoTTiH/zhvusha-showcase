def get_workspace_path(raw_path: str) -> Path:
    """Expand ~ and return absolute workspace path."""
    return Path(raw_path).expanduser().resolve()


async def ensure_workspace(root: Path, *, admin_user_id: int | None = None) -> None:
    """Create workspace directory structure and seed files if missing."""
    root.mkdir(parents=True, exist_ok=True)

    for dir_rel in WORKSPACE_DIRS:
        (root / dir_rel).mkdir(parents=True, exist_ok=True)

    for file_rel, content_fn_name in MANAGED_FILES.items():
        file_path = root / file_rel
        content_fn = globals()[content_fn_name]
        file_path.write_text(content_fn(), encoding="utf-8")
        logger.info("workspace_managed_file_written", path=str(file_path))

    for file_rel, content_fn_name in SEED_FILES.items():
        file_path = root / file_rel
        if not file_path.exists():
            content_fn = globals()[content_fn_name]
            file_path.write_text(content_fn(), encoding="utf-8")
            logger.info("workspace_seed_file_created", path=str(file_path))

    if admin_user_id is not None:
        _ensure_admin_profile(root, admin_user_id)

    logger.info("workspace_ensured", root=str(root))


def _ensure_admin_profile(root: Path, admin_user_id: int) -> None:
    """Create Nikita's pre-populated profile if it doesn't exist."""
    profile_dir = root / "memory" / "people" / str(admin_user_id)
    profile_file = profile_dir / "profile.md"
    if profile_file.exists():
        return

    profile_dir.mkdir(parents=True, exist_ok=True)
    profile_file.write_text(
        f"""\
---
user_id: {admin_user_id}
username: nikita
first_name: Nikita
significance: inner_circle
interaction_count: 0
first_seen: 2026-03-31T00:00:00+00:00
last_seen: 2026-03-31T00:00:00+00:00
---

# Заметки

Создатель. Его фидбек — мой компас.
""",
        encoding="utf-8",
    )
    logger.info("admin_profile_created", user_id=admin_user_id)


